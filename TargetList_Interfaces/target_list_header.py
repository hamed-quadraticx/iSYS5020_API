import logging
import numpy as np

logging.basicConfig(format='%(levelname)s - %(asctime)s : %(message)s',datefmt='%H:%M:%S',level=logging.DEBUG)

class Target_List_Header :
    frame_id = np.uint(0x0000)
    FW_major = np.uint(0x0000)
    FW_fix = np.uint(0x0000)
    FW_minor = np.uint(0x0000)
    no_of_detections = np.uint(0x0000)
    no_of_targets = np.uint(0x0000)
    crc = np.uint32(0x00000000)
    bytes_per_target = np.uint(0x0000)
    no_of_packets = np.uint(0x0000)
    _packet_length = 256

    def __init__(self,header_data_arr):
        self.header_data_arr = header_data_arr
    
    def parse(self):
        try :
            # check packet is not none and packet length must be equal 256 according to the documentation
            if self.header_data_arr is None or len(self.header_data_arr) != self._packet_length :
                logging.warning('Invalid header packet')
                return False
            else :
                self.frame_id =int.from_bytes(self.header_data_arr[0:1], "little")
                self.FW_major =int.from_bytes(self.header_data_arr[2:3], "little")
                self.FW_fix =int.from_bytes(self.header_data_arr[4:5], "little")
                self.FW_minor =int.from_bytes(self.header_data_arr[6:7], "little")
                self.no_of_detections =int.from_bytes(self.header_data_arr[8:9], "little")
                self.no_of_targets =int.from_bytes(self.header_data_arr[10:11], "little")
                self.crc =int.from_bytes(self.header_data_arr[12:15], "little")
                self.bytes_per_target =int.from_bytes(self.header_data_arr[16:17], "little")
                self.no_of_packets =int.from_bytes(self.header_data_arr[18:19], "little")

                logging.info(f'frame ID =  {self.frame_id}') 
                logging.info(f'Major =  {self.FW_major}') 
                logging.info(f'fix =  {self.FW_fix }')
                logging.info(f'Minor =  {self.FW_minor }')
                logging.info(f'no of detections  =  {self.no_of_detections }')
                logging.info(f'no of targets  =  {self.no_of_targets }')
                logging.info(f'crc =  {self.crc }')
                logging.info(f'bytes per target =  {self.bytes_per_target}') 
                logging.info(f'no of packets =  {self.no_of_packets }')
                return True
        except Exception as exception_error:
            logging.warning('Invalid header packet')
            return False

